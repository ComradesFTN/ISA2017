package isa.projekat.domain;

public class Auditorium {

	private String name;

	private int size=0;
	
	public Auditorium(){
		
	}	

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}
	
	
	
}
